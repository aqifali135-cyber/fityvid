export function getCategoryItems(categories, categoryName) {
  if (!categoryName || categoryName === 'All') return categories.All || [];
  return categories[categoryName] || [];
}

export function isFlagItem(item) {
  return Boolean(item && typeof item === 'object' && item.image && item.copyValue);
}

export function filterBySearch(items, query) {
  const q = (query ?? '').trim().toLowerCase();
  if (!q) return items;
  return items.filter((item) => {
    if (typeof item === 'string') return item.toLowerCase().includes(q);
    if (!item || typeof item !== 'object') return false;

    const code = (item.code || '').toLowerCase();
    const label = (item.label || '').toLowerCase();
    const aliases = (item.aliases || []).map((alias) => String(alias).toLowerCase());

    if (code === q) return true;
    if (aliases.includes(q)) return true;
    if (q.length <= 2) return code.startsWith(q);
    if (label.includes(q)) return true;
    if (aliases.some((alias) => alias.includes(q))) return true;
    return false;
  });
}

export function countWords(text) {
  const value = text ?? '';
  const trimmed = value.trim();
  return {
    words: trimmed ? trimmed.split(/\s+/).length : 0,
    characters: value.length,
    charactersNoSpaces: value.replace(/\s/g, "").length,
    sentences: trimmed ? (trimmed.match(/[.!?]+(\s|$)/g) || []).length || 1 : 0,
    paragraphs: trimmed ? trimmed.split(/\n+/).filter(Boolean).length : 0,
  };
}
